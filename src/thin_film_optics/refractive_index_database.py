def _interpolate_index_refraction(
    *,
    wavelength_oi: Any,
    wavelength_db: Any,
    refractive_index_db: Any,
    coefficient_extinction_db: Any,
) -> Any:
    """Interpolation utility to build the index of refraction in complex format.

    Args:
        wavelength_oi (ndarray): Wavelength range of interest.
        wavelength_db (ndarray): Wavelength available in the DB for the index of refraction.
        refractive_index_db (ndarray): Index of refraction of the material from the DB.
        coefficient_extinction_db (ndarray[complex]): Extinction coefficient of the
        material from the DB.

    Returns:
        (ndarray[complex]): Complex index of refraction.
    """

    if np.all(wavelength_db[0] <= wavelength_oi <= wavelength_db[-1]):

        # Interpolation objects for real and imag part
        itp_n = scipy.interpolate.interp1d(wavelength_db, refractive_index_db)
        itp_k = scipy.interpolate.interp1d(wavelength_db, coefficient_extinction_db)

        # Complex index of refraction
        refractive_index = itp_n(wavelength_oi) + 1j * itp_k(wavelength_oi)

        return refractive_index

    raise ValueError("the input wavelength_oi is not supported for this material.")


