def tio2_crystal(*, wavelength: Any) -> Any:
    """Returns the index of refraction of TiO2 crystal in complex form. (J. R. Devore.
    Refractive indices of rutile and sphalerite, J. Opt. Soc. Am. 41, 416-419 (1951))

    Args:
        wavelength (ndarray): Wavelength range of interest. (list, float, integer or numpy
        arange, range)

    Returns:
        (ndarray): Complex index of refraction of TiO2 crystal.
    """
    c_1, c_2, c_3 = 5.913, 0.2441, 0.0803
    n = np.sqrt(c_1 + c_2 / ((wavelength * 1e-3) ** 2 - c_3))
    n += 1j * 0.0

    return n.reshape(-1)


